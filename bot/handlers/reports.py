@router.callback_query(F.data.startswith("period_"))
async def process_period_selection(callback: CallbackQuery, state: FSMContext):
    """
    –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –¥–ª—è –≤—ã–±–æ—Ä–∞ –ø—Ä–µ–¥—É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω–æ–≥–æ –ø–µ—Ä–∏–æ–¥–∞
    """
    period = callback.data.split("_")[1]
    
    # –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â—É—é –¥–∞—Ç—É
    today = datetime.now()
    
    # –í—ã—á–∏—Å–ª—è–µ–º –¥–∞—Ç—ã –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ –ø–µ—Ä–∏–æ–¥–∞
    if period == "week":
        # –ù–µ–¥–µ–ª—è
        start_date = (today - timedelta(days=7)).strftime("%d.%m.%Y")
        end_date = today.strftime("%d.%m.%Y")
    elif period == "month":
        # –ú–µ—Å—è—Ü
        start_date = (today - timedelta(days=30)).strftime("%d.%m.%Y")
        end_date = today.strftime("%d.%m.%Y")
    elif period == "quarter":
        # –ö–≤–∞—Ä—Ç–∞–ª (3 –º–µ—Å—è—Ü–∞)
        start_date = (today - timedelta(days=90)).strftime("%d.%m.%Y")
        end_date = today.strftime("%d.%m.%Y")
    else:
        await callback.answer("–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π –ø–µ—Ä–∏–æ–¥")
        return
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –¥–∞—Ç—ã –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏
    await state.update_data(start_date=start_date, end_date=end_date)
    
    # –ü–µ—Ä–µ—Ö–æ–¥–∏–º —Å—Ä–∞–∑—É –∫ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—é
    await show_report_confirmation(callback.message, state)
    await callback.answer()

# –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –≤–≤–æ–¥–∞ –Ω–∞—á–∞–ª—å–Ω–æ–π –¥–∞—Ç—ã
